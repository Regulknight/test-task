CREATE TABLE IF NOT EXISTS PUBLIC.GENRE(ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
  NAME VARCHAR(30) NOT NULL);
CREATE TABLE if NOT EXISTS PUBLIC.AUTHOR(ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
  FNAME VARCHAR(30) NOT NULL,
  LNAME VARCHAR(30) NOT NULL,
  PATRON VARCHAR(30));
CREATE TABLE if NOT EXISTS PUBLIC.BOOK(ID BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 0) NOT NULL PRIMARY KEY,
  NAME VARCHAR(30) NOT NULL,
  BYEAR DATE NOT NULL,
  CITY VARCHAR(30) NOT NULL,
  AUTHOR BIGINT NOT NULL,
  GENRE BIGINT NOT NULL,
  PUBLISHER VARCHAR(30) NOT NULL,
  CONSTRAINT FOREIGN_KEY_FOR_AUTHOR FOREIGN KEY(AUTHOR) REFERENCES PUBLIC.AUTHOR(ID),
  CONSTRAINT FOREIGN_KEY_FOR_GENRE FOREIGN KEY(GENRE) REFERENCES PUBLIC.GENRE(ID));